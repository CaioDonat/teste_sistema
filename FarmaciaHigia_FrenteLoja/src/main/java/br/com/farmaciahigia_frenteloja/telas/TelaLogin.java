    public void logar(){
        
        String sql = "select * from users where login=? and senha=?";
        
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, fieldUsuario.getText());
            pst.setString(2, String.valueOf(fieldPassword.getPassword()));
            
            System.out.println(pst);
            rs = pst.executeQuery();
            System.out.println(rs);
            
            if (rs.next()){
                TelaInicial telaHome = new TelaInicial();
                telaHome.setUsuario(fieldUsuario.getText());
                telaHome.setVisible(true);
                this.dispose();
                conexao.close();
            }else{
                JOptionPane.showMessageDialog(null, "deu Ruim");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }
    
